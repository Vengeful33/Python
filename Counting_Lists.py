#Lets say you want to find out how much, but only true or false, then we need to use the in statement

Groceries = ["Bacon", "Eggs", "Milk"]
has_bacon = "Bacon" in Groceries

print(has_bacon)

#This will then print true, we are also able to print the in loop by itself as well.