
#And statements can be used to make more conditions in order to print true or false:

Color1 = "Red"
Coler2 = "Blue"
if Color1 == "Red" and Coler2 == "Blue":
    print("Purple")

#If one of the and conditions is false, it will not print, you can also add as many and conditions as you would like in the statement:

Year = 2002
Day = 16
Month = "April"
if Year == 2002 and Day == 16 and Month == "May":
    print("That is my birthday")

#Or statments can be used to make only one condition in a statement to be true or false:

Time_Hour = 6
Time_Minute = 30
if Time_Hour == 7 or Time_Minute == 30:
    print("It is Dinner Time")

