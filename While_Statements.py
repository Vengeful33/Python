
#While Statements are used to make a infinite loop:

while True:
    print ("True")

#While statements only run again once all the code below is ran. If you need to turn off a while statement, turn the variable false.

#If the boolean value is false, the code will not run

while False:
    print("No")

Day = 10
while Day == 10:
    print(Day)
    Day = 12

#Beacause the variable get changed after the loop, it only prints once