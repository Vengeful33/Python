
#Just like len(), we can use min() to find the smallest value in a list

EmployeeNumber = [11, 6, 45, 7]

print(min(EmployeeNumber))

#This prints 6 since it is the smallest number

#We can also use maximum to find the bigest number

Data = [12, 678, 458, 1,]
MaxData = max(Data)

print(MaxData)

#This will print 678, used a variable up top for reference